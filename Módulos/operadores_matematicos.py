prova1 = 10 
prova2 = 5

# Soma
resultado1 = prova1 + prova2
print(resultado1) # 15
# Subtração
resultado2 = prova1 - prova2
print(resultado2) # 5
# Multiplicação
resultado3 = prova1 * prova2
print(resultado3) # 50
# Divisão
resultado4 = prova1 / prova2
print(resultado4) # 2
# Potência 
resultado5 = prova1 ** prova2
print(resultado5) # 32
# Divisão por inteiro
resultado6 = prova1 // prova2
print(resultado6) # 2
# Módulo (Resto da divisão)
resultado7 = prova1 % prova2
print(resultado7) # 0