prova1 = 10
prova2 = 5

# Se o valor for igual retorna 'True', se for diferente retorna 'False'
resultado = prova1 == prova2

print(resultado) # False


# Se o valor for maior retorna 'True', se for menor retorna 'False'
resultado2 = prova1 > prova2

print(resultado2) # True

# Se o valor for menor retorna 'True', se for maior retorna 'False'
resultado3 = prova1 < prova2 

print(resultado3) # False

# Se o valor for maior ou igual retorna 'True', se for menor retorna 'False'
resultado4 = prova1 >= prova2

print(resultado4) # True

# Se o valor for menor ou igual retorna 'True', se for maior retorna 'False'
resultado5 = prova1 <= prova2

print(resultado5) # False